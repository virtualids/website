	<h1>about virtual information display system</h1>

	<br>

	<p>
		vids is a system of information display systems that can be used by controllers on vatsim. it's intended use is for vatusa, but with special request, may be adapted for international use. 
		vids is not required for controllers. it should be seen as an optional tool for anyone who would like to use it. our goal is to recreate the real world systems used in air traffic control. 
		this tool should NOT be used for real world use. it is strictly for simulator use. for any questions, please join our <strong><a href="https://discord.gg/YfpXqFRG3H" target="_blank">discord</a></strong>
